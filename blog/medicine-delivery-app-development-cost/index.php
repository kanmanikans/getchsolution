                            </h2>
                            <ul>
                                <p class="pl-15"><b>1. Programming Languages:</b> The development team can use languages
                                    such as Java, Swift, or Kotlin to develop the app.
                                </p>

                                <p class="pl-15"><b>2. Backend Development:</b> The app’s backend can be built using
                                    frameworks such as Node.js or Django.
                                </p>
                                <p class="pl-15"><b>3. Database:</b> The app can use databases such as MySQL or MongoDB
                                    for data storage.
                                </p>
                                <p class="pl-15"><b>4. Cloud Services:</b> The app can use cloud services such as Amazon
                                    Web Services or Microsoft Azure for hosting and data storage.
