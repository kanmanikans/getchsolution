                            <p>All successors of the iPhone 7 do not have audio ports to connect earphones. If one wants
                                to listen to music or any other type of audio from an iPhone, he or she has to connect a
                                Bluetooth-enabled pair of earphones or headphones or buy and connect a C-Type to USB
                                cable with the C-Type end being a Lightning Connector that’s supported by iPhones.</p>
                            <h3>Easy And Cheap Availability Of Android Phone Hardware</h3>
                            <p>Most of the Android smartphones today can quickly charge via power adapters with C-Type
                                cables. The hardware/gadgets required for Android smartphones are easily available in
                                the market and are cost-effective.</p>
