                            <h2 id="1">How The Android OS Emerged At The Top</h2>
                            <p>The Android mobile OS platform was founded in October, 2003. It was founded by Palo Alto,
                                California. Rich Miner, Nick Sears, Chris White, and Andy Rubin. The primary purpose of
                                developing the Android OS was to incorporate the GPS feature in it so that people’s
                                mobile phones could track the owner’s location.</p>
                            <p>Later, in the eventful year of 2005, the tech behemoth Google had acquired Android. It
                                decided to use the Linux coding technology to make the Android OS more sophisticated
                                step by step. Adopting Linux as the coding technology for the Android OS enabled Google
                                to make the OS development project, an open-source one.</p>
                            <p>As a result, in a very short period of time, a plethora of advanced versions of the
                                Android OS were developed and released into the market. Many useful apps were developed
                                to make them compatible with Android.</p>
                            <p>Today, the only serious competitor for the Android OS is Apple’s iOS. Even that is a
                                distant second to Android with iOS having only 29.49% market share. Android on the other
                                hand has a whopping 69.74% of the market share of mobile OSs. The Windows OS has become
                                almost obsolete with only 0.77% of the market share of mobile OSs. Android had
                                eliminated its former competitors like BlackBerry, Palm OS, Web OS, etcetera before
                                emerging at the top spot among the most used mobile OSs.</p>
                            <p>Today, Android 12 is the latest version of the Android OS. Google had undertaken an
                                ambitious project called “Fuchsia” in 2019 to make an Android OS version that could
                                support all types of apps, including apps meant for the iOS and Windows OS so that
                                people do not even have to think about buying an iPhone, an iPad, a Windows smartphone,
                                a desktop, or a even a laptop.</p>
                            <p>If the “Fuchsia” version of the Android OS is released successfully in the market, it
                                could unhinge all of its competitors unless they come up with such an advanced
                                cross-platform mobile OS soon.</p>
